package nc.box2d.adventure.sprit;

public class Sprite {
	int a;
	int b; 
	int c;
	int d;
	public Sprite(int a, int b, int c, int d) {
		super();
		this.a = a;
		this.b = b;
		this.c = c;
		this.d = d;
	}
	public int getA() {
		return a;
	}
	public int getB() {
		return b;
	}
	public int getC() {
		return c;
	}
	public int getD() {
		return d;
	}
}
